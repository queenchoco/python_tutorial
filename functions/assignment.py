def sub(num1, num2):
    total = num1 - num2 
    print(f'The result of {num1} - {num2} is {total}')
   

def mul(num1, num2):
    total = num1 * num2
    print(f'The result of {num1} * {num2} is {total}')


def mod(num1, num2):
    total = num1 % num2
    print(f'The result of {num1} % {num2} is {total}')


def div(num1, num2):
    total = num1 / num2
    print(f'The result of {num1} / {num2} is {total}')



sub(30, 5)
mul(15, 5)
mod(45, 7)
div(45, 4)
